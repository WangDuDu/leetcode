            if (num == 0) {
                return "0";
            }

            long longNum = num;
            if (longNum < 0) {
                longNum = (long)(Math.pow(2, 32) + num);
            }
            StringBuilder stringBuilder = new StringBuilder();
            while (longNum != 0) {
                long bitNum = longNum % 16;
                char c = (char) bitNum;
                if (bitNum >= 10) {
                    c = (char) ('a' + (bitNum - 10));
                } else {
                    c = (char) ('0' + bitNum);
                }
                stringBuilder.append(c);
                longNum = longNum / 16;
            }
            return stringBuilder.reverse().toString();
